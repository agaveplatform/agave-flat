package org.iplantc.service.jobs.exceptions;

public class AtmosphereException extends Exception {

	private static final long serialVersionUID = 413078133053877189L;

	public AtmosphereException() {}
	
	public AtmosphereException(String arg0)
	{
		super(arg0);
	}

	public AtmosphereException(Throwable arg0)
	{
		super(arg0);
	}

	public AtmosphereException(String arg0, Throwable arg1)
	{
		super(arg0, arg1);
	}

}
