package org.iplantc.service.jobs.exceptions;

public class VirtualMachineException extends Exception {

	private static final long serialVersionUID = 390760557864988862L;

	public VirtualMachineException()
	{}

	public VirtualMachineException(String arg0)
	{
		super(arg0);
	}

	public VirtualMachineException(Throwable arg0)
	{
		super(arg0);
	}

	public VirtualMachineException(String arg0, Throwable arg1)
	{
		super(arg0, arg1);
	}

}
