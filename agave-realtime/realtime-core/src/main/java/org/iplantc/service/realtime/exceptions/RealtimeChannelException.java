package org.iplantc.service.realtime.exceptions;

public class RealtimeChannelException extends Exception {

	private static final long serialVersionUID = -4674167421857392526L;

	public RealtimeChannelException() {}

	public RealtimeChannelException(String arg0)
	{
		super(arg0);
	}

	public RealtimeChannelException(Throwable arg0)
	{
		super(arg0);
	}

	public RealtimeChannelException(String arg0, Throwable arg1)
	{
		super(arg0, arg1);
	}

}
