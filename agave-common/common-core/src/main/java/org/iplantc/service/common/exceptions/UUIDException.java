package org.iplantc.service.common.exceptions;

public class UUIDException extends Exception {

	private static final long serialVersionUID = -1165117734722140904L;

	public UUIDException() {}

	public UUIDException(String arg0)
	{
		super(arg0);
	}

	public UUIDException(Throwable arg0)
	{
		super(arg0);
	}

	public UUIDException(String arg0, Throwable arg1)
	{
		super(arg0, arg1);
	}

}
