package org.iplantc.service.common.exceptions;

public class AgaveNamespaceException extends Exception {

	private static final long serialVersionUID = 2755608534179457989L;

	public AgaveNamespaceException() {}

	public AgaveNamespaceException(String arg0)
	{
		super(arg0);
	}

	public AgaveNamespaceException(Throwable arg0)
	{
		super(arg0);
	}

	public AgaveNamespaceException(String arg0, Throwable arg1)
	{
		super(arg0, arg1);
	}

}
