package org.iplantc.service.common.exceptions;

public class MyProxyGatewayException extends Exception {

	private static final long serialVersionUID = -4439533061351969354L;

	public MyProxyGatewayException() {
		super();
	}

	public MyProxyGatewayException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public MyProxyGatewayException(String arg0) {
		super(arg0);
	}

	public MyProxyGatewayException(Throwable arg0) {
		super(arg0);
	}

}
